package omok;

public class OmokDTO {
	private int xPoint;
	private int yPoint;
	private int sw;
	
	public OmokDTO() {
		
	}
	
	public int getSw() {
		return sw;
	}
	
	public void setSw(int sw) {
		this.sw = sw;
	}

	public int getxPoint() {
		return xPoint;
	}

	public void setxPoint(int xPoint) {
		this.xPoint = xPoint;
	}

	public int getyPoint() {
		return yPoint;
	}

	public void setyPoint(int yPoint) {
		this.yPoint = yPoint;
	}
	
	
}
